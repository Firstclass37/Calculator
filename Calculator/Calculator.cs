using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Calculator
{
    public class Calculator
    {
        public int Sum(int inpu1, int input2)
        {
            return inpu1 + input2;
        }
        public int Sub(int inpu1, int input2)
        {
            return inpu1 - input2;
        }
        public int Dev(int inpu1, int input2)
        {
            return inpu1 / input2;
        }
        public int Mul(int inpu1, int input2)
        {
            return inpu1 * input2;
        }
    }
}
